    private boolean sprawdzTekst(String s){
        boolean poprawny = true;
        for(int i = 0; i < s.length(); ++i){
            char c = s.charAt(i);
            if(!(Character.isLetter(c) || Character.isDigit(c) || Character.isWhitespace(c) || (c == '.') || (c == ',')|| (c == '-'))){
                poprawny = false;
                break;
            }
        }


        return poprawny;
    }
    
